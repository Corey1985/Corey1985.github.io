////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//compare 2 numbers & return the smallest one
//
function min(num1, num2){
  if(num1 < num2){
    return num1;
  }
  else{
    return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//check if number is even
//if even return true
//if odd return false
function isEven(num) {
  if(num % 2 === 0){
    return true;
  }
  else{
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//compare a string to a letter
//if the letter exists inside of the string add it to a count
//edge case: the letter or string may be upper or lower case but still needs to be counted.
function countChars(string, letter) {
  let evenStr = string.toLowerCase();
  let evenLtr = letter.toLowerCase();
  let stringCount = 0;
  for(let i = 0; i < evenStr.length; i++){
    if(evenStr[i] === evenLtr){
      stringCount++;
    }
  }
  return stringCount;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//count the upper case B's in the string

function countBs(string) {
  let stringCount = 0; 
  for(let i = 0; i < string.length; i++){
    if(string[i] === "B"){
      stringCount++;
    }
  }
  return stringCount;
}

